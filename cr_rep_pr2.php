              <h1 class="title-h1">What is a credit report?</h1>
              <p >You are the source of great interest, and profit, of many national companies. These companies, known as “consumer reporting agencies” under the Fair Credit Reporting Act, gather financial and other information about you and millions of other consumers across the country. They use this information to make a profit – that’s right, they sell your information to make a buck.</p>
            </div>
            
            <br>

            <div class="cri-style2">
              <h2 style="font-size: 30px; font-weight: normal;">The FCRA defines a consumer reporting agency as:</h2>

              <p>Any person which, for monetary fees, dues, or on a cooperative nonprofit basis, regularly engages in whole or in part in the practice of assembling or evaluating customer credit information or other information on consumers for the purpose of furnishing consumer reports to third parties, and which uses any means or facility of interstate commerce for the purpose of preparing or furnishing consumer reports. These consumer reporting agencies sell your information to businesses and individuals with a “legitimate” need to know something about you. Most often, these businesses are creditors that want to lend you money or otherwise provide you with credit. But, the consumer reporting agencies have many customers that are eager for your information, including prospective employers, landlords, and insurance companies .</p>

              <p>All of these businesses and individuals are trying to decide whether you’re a “good risk” for whatever it is they’re offering to you. If your credit report contains mostly “good” information, then you’ll probably receive the loan or service. But, if your credit report contains mostly “bad” information, then you’re probably not going to get the loan or service. We’re going to closely examine the information that’s contained in your credit reports in a moment, and then we’re going to talk about how to fix that information – because we guarantee that some of it is wrong. But for now, let’s take a close look at the major consumer reporting agencies.(As with the term “credit report,” the term “credit bureau” never appears in the FCRA. All of the companies that we typically think of and call “credit bureaus” are in fact consumer reporting agencies. As with the term “credit report,” a “credit bureau” is really a sub-set of consumer reporting agencies that primarily collect, maintain, and sell credit information to third parties, as opposed to information regarding medical or insurance claim histories, for example. For our purposes, we’ll refer to the major consumer reporting agencies as “credit bureaus,” because that is the generally accepted term for them.)</p>
            </div>
